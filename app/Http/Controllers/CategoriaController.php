    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'categorianome' => 'required|max:280'
        ]);

        $categoria = new Categoria();
        $categoria->categorianome = $request['categorianome'];
        $categoria->slug = Str::slug($request['categorianome']);
        $latestSlug = Categoria::whereRaw("slug RLIKE '^{$categoria->slug}(-[0-9]*)?$'")
        ->latest('id')
        ->value('slug');
            if ($latestSlug){
                $pieces = explode('-', $latestSlug);
                $number = intval(end($pieces));
                $categoria->slug .= '-' . ($number + 1);
            }
        $categoria->save();
        return redirect('home');

    }

